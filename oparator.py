# Arithmetic Operator perform simple calculations
X = 45
Y = 21

print(X + Y)
print(X - Y)
print(X * Y)
print(X / Y)
print(X % Y)  # returns the remainder after dividing two numbers

# comparison operators
a = 56
b = 2
print(a < b)
print(a > b)
print(a <= b)  # Less than or equal to
print(a >= b)  # Greater than or equal to
print(a == b)  # Equal to
print(a != b)  # Not equal to

# Assignment Operators
z = 6
print(z)

num = 2
num %= 10
print(num)

# Logical Operators
num1 = 5
num2 = 10
print(num1 < num2 and num1 == num2)
print(num1 < num2 or num1 == num2)
print(not (num1 < num2 and num1 == num2))
