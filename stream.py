text = "hello"
text1 = "ThERE"

print(text)

# Accessing an element in a string
print(text[2])

# modifying a string
print(text.upper())
print(text.lower())

# Size/length of a string
print(len(text))

# string Concatenation - Combining string
print(text + " " + text1)

# Assignment
# 1. Reassigning a string
text = ""
print(text)
# 2. Deleting  string
text1 = ""
print(text1)
